<template>
  <p>
    Combat unfolds in rounds, with each round taking 5 seconds of in game time.
  </p>
  <p>
    All players take their turn at the same time and then the monsters take
    their turn. If the players have trouble then the DM can decide the order of
    the players.
  </p>
  <p>
    Unless the players run into a trap, ambushed or they are surprised, they
    will always go first then the monsters take their turn.
  </p>
  <p>
    Each monster has a predefined actions for each round. The DM will roll to
    determine what the monster does for this round.
  </p>
  <p>At the end of round all player character's reduce their endurance by 1.</p>
</template>
