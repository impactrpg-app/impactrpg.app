<template>
  <p>Instead of attacking, you can perform a maneuver.</p>
  <p>
    A maneuver is a special attack that can be used to disarm, knockdown, or
    intimidate a target.
  </p>
  <p>
    To perform a maneuver, describe the maneuver to the DM, then roll an attack
    as usual.
  </p>
  <p>
    If you have more successes than the target's defense roll then the maneuver
    is successful.
  </p>
  <h5>Example Maneuvers</h5>
  <p>
    <b class="text-contrast">Grab:</b> The target can only attack you for 1
    round.
  </p>
  <p>
    <b class="text-contrast">Knockdown:</b> The target's Agility is reduced to 0
    for 1 round.
  </p>
  <p>
    <b class="text-contrast">Intimidate:</b>
    The target suffers a penalty to their attack rolls.
  </p>
</template>
