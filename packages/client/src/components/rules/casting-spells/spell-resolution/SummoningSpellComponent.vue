<template>
  <h3>SUMMONING SPELLS</h3>
  <p>
    To summon a creature you must roll dice equal to the amount of mana spent on the creature.
    The creature will be able to take wounds equal to the successes.    
  </p>
  <p>
    After the creature is summoned you must give the creature a purpose.
    The creature will act out the purpose you have described and once fulfilled or upon resting 
    the creature will disappear.
  </p>
  <p>
    When you summon a creature it does not have any actions besides a basic attack using it's strength. 
  </p>
  <p>
    When you cast a spell instead of the spells effect taking place you can imbue the spell to the creature.
    This will make the creature able to cast the spell you imbued into the creature. 
    The creature will always cast the spell with the same amount of mana you imbued into it. <b>It does not roll dice.</b>
  </p>
  <p>
    All of the creatures abilities are set to 1. You may use blessings to increase these.
  </p>
  <p>
    You do not decide what the creature does, it will act out the purpose you have described.
  </p>
</template>