<template>
  <p>To attack, spend 1 action and 1 endurance.</p>
  <h4>Roll to Attack</h4>
  <p>Perform a Strength check, adding your weapon's attack value as bonus dice.</p>
  <h4>Defender's Roll</h4>
  <p>The defender makes an Agility check, adding their armor value as bonus dice.</p>
  <h4>Resolve the Outcome</h4>
  <p>Each success on the attack roll represents a potential wound.</p>
  <p>
    The defender cancels one wound for each success on their defense roll. Any uncanceled
    wounds are applied to the defender.
  </p>
  <p>
    If the defender takes 3 or more wounds from a single attack, they also suffer an
    Injury.
  </p>
  <p>
    Defenders do not deal damage to attackers, even if their defense roll exceeds the
    attack roll.
  </p>
</template>
